package com.telran.base.lesson7;

public class FirstTime {

    public static void main(String[] args) {
        for (int i = 10; i < 9; i++) {
            System.out.println("Hello");
        }

        int coutn = 10;
        while (coutn < 10) {
            System.out.println("Hello from while");
            coutn++;
        }
    }
}
